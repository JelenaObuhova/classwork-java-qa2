package homework1;

public class Train {
    private Number trainNumber;
    private String trainDirection;
    private String fuel;

    public Number getTrainNumber() {
        return trainNumber;
    }

    public void setTrainNumber(Number trainNumber) {
        this.trainNumber = trainNumber;
    }

    public String getTrainDirection() {
        return trainDirection;
    }

    public void setTrainDirection(String trainDirection) {
        this.trainDirection = trainDirection;
    }

    public String getFuel() {
        return fuel;
    }

    public void setFuel(String fuel) {
        this.fuel = fuel;
    }
}
